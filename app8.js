// prgm 1
let prgm1 = prompt(10);

document.write('The Value of a is :' +prgm1)

document.write('<br>');
document.write('<br>');
// prgm2
let prgm2 = ++prgm1;
document.write('The Value of ++a is' + prgm1);
document.write('<br>');
document.write('Now the Value of a is:' +prgm2);

document.write('<br>');
// prgm3
let prgm3 = prgm2++;
document.write('<br>');
document.write('The Value of a++ is' + prgm3);

document.write('<br>');
document.write('Now the Value of a is:' + ++prgm3);

// prgm4
document.write('<br>');
let prgm4 = --prgm3;
document.write('<br>');
document.write('The Value of --a is' + prgm4);

document.write('<br>');
document.write('now the Value of a is:' +prgm4);

// prgm5
document.write('<br>');
let prgm5 = prgm4--;
document.write('<br>');
document.write('The Value of a-- is' + prgm5);

document.write('<br>');
document.write('The Value of a is:' + --prgm5);


